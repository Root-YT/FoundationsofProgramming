public class App {
    public static void main(String[] args) throws Exception {
        System.out.println("Pre-Condition Loops");

        System.out.println("For Loop");
      for(int i=0; i<10; i++)
        {
        System.out.println(i);
        }


        System.out.println("While Loop");
        int i = 0;
        while (i < 10) {
        System.out.println(i);
        i++;
        }
    }
}
